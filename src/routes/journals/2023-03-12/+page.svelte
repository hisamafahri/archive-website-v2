	<h3 class="head-section">What is a Product?</h3>
	<p>
		Whether you are a startup founder, entrepreneur, engineer, or anything
		similar you might find yourself in the process of building something (in
		this case, let's call it a product). If you want to take it further and be
		much more philosophical in this case, you can include something like
		improving your skills, building relationships, or even fixing a broken roof
		in your house as a <i>product</i>.
	</p>
	<br />
	<p>
		But, for simplicity's sake let's focus our context on a software product.
	</p>
	<h3 class="head-section">The Nightmare of the Problem Solver</h3>
	<p>
		No matter what your reason for building a product (money, self-fulfilment,
		fun), having the right execution is the key.
	</p>
	<br />
	<p>
		I've seen enough cases where building great things in the wrong way can mess
		up the end products and eventually destroy the product and its vision. A lot
		of people tend to forget that the building process (execution process) of
		building a product itself is one of the most crucial processes.
	</p>
	<br />
	<div class="blockquote">
		<p>
			The product itself might have a great vision, exactly pinpointing and
			addressing its user's problems. But if the execution of building that
			solution is a mess, the user will end up in a whole other stack of
			problems: <b>your product itself</b>.
		</p>
	</div>
	<h3 class="head-section">So, What is the 'Right' Way?</h3>
	<p>
		Okay, let's assume you have the problem and you know how to address and
		solve that problem. Now you need to validate your problem and solution by
		building a product in this case.
	</p>
	<br />
	<p>
		To build a product the 'Right' way, basically, all you need is a great <b
			>creator and user experience</b
		>. That's it, as simple as that.
	</p>
	<h3 class="head-section">Great Creator Experience</h3>
	<p>
		Building a product is hard. But you, as the creator, should enjoy it. If you
		don't enjoy it, how the user will enjoy your product later on?
	</p>
	<br />
	<p>
		In the case of a software product, choosing the stack you and your team love
		and are familiar with, and having everything structured, designed, and
		managed properly, will greatly improve your creation experiences.
	</p>
	<br />
	<p>
		I've seen so many people end up not delivering their product just because
		they don't enjoy the building process by using weird and unnecessary
		technologies "just because everyone uses it".
	</p>
	<br />
	<p>
		If your product's stage is still in an MVP stage, this creator experience
		thing becomes even much more important. Why? because you need to iterate so
		much. I never said that you should overengineer and make your product really
		overkill, or on the other hand even underkill in the MVP stages (or any
		stage for that matter), all you need is that sweet spot.
	</p>
	<br />
	<p>
		If <a
			href="https://www.levels.fyi/blog/scaling-to-millions-with-google-sheets.html"
			target="_blank"
			>using Google Sheets as your backend to serve millions of users</a
		> is your sweet spot, go for it.
	</p>
	<h3 class="head-section">Great User Experience</h3>
	<p>
		This part is what often boils me so much. A lot of people sacrifice the user
		experience so much in the name of <i
			>"Hey, my product is just in an MVP stage"</i
		>.
	</p>
	<br />
	<p>
		No matter what stage your product is, if the user's experience of using your
		product itself is hell, then your product is bound to fail.
	</p>
	<br />
	<p>
		Great user experience here is not that your product must be very well
		designed, there are teams of UX researchers spending the majority of your
		budget for this MVP, no. It is the opposite.
	</p>
	<br />
	<p>
		You as the product builder (the problem solver) need to always make sure
		that your product is aligned with the user's needs. Your product needs to
		exactly hit the point of the user. <b>AND</b>, your user can easily identify
		the value of you and your product.
	</p>
	<h3 class="head-section">Conclusion?</h3>
	<p>
		In the end, every choice is up to you as the creator. What I can say is that
		you need to always put the user's needs first. Thank you. :)
	</p>

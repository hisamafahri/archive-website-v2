<div class="content">
	<h3 class="head-section">What is a Product?</h3>
	<p>
		Whether you are a startup founder, enterpreneur, engineer, or anything similiar you might find
		yourself in a process of building something (in this case, let's call it as a <i>product</i>).
		If you want to take it further and be much more phylosophical in this case, you can include
		something like improving your skills, building a realtionships, or even fixing a broken roof in
		your house as a <i>product</i>.
	</p>
	<br />
	<p>But, for simplicity sake let's focusing our context to a Software product.</p>
	<h3 class="head-section">The Nightmare of the Problem Solver</h3>
	<p>
		No matter what's your reason on building a product (money, self fullfillment, fun), having the
		right execution is the key.
	</p>
	<br />
	<p>
		I've seen enough cases where building a great things in a wrong way can messed up the end
		products and eventually destroy the product and its vision. A lot of people tend to forget that
		the building process (execution process) of building a product itself is a one of the most
		curcial process.
	</p>
	<br />
	<div class="blockquote">
		<p>
			The product itself might have a great vision, exactly pinpoint and addressing its user's
			problems. But if the execution of building that solution is a mess, the user will ended up in
			a whole other stack of problem: <b>your product itself</b>.
		</p>
	</div>
	<h3 class="head-section">So, What is the 'Right' Way?</h3>
	<p>
		Okay, let's assume you have the problem and you know how to address and solve that problem. Now
		you need to validate yout problem and solution by building a product in this case.
	</p>
	<br />
	<p>
		To build a product the 'Right' way, basicaly all you need is a great <b
			>creator and user experiences</b
		>. That's it, as simple as that.
	</p>
	<h3 class="head-section">Great Creator Experience</h3>
	<p>
		Building a product is hard. But you, as the creator, should enjoy it. If you don't enjoy, how
		the user will enjoy your product later on.
	</p>
	<br />
	<p>
		In case of a software product, choosing the stack you and your team love and familiar, have
		everything structured, designed, and managed properly, will greatly improve your creation
		experiences.
	</p>
	<br />
	<p>
		I've seen so much people ended up not delivering their product just because they don't enjoy the
		building process by using weird and unnecessary technologies "just because everyone use it".
	</p>
	<br />
	<p>
		If your product's stage is still is in an MVP stage, this creator experience thing becomes even
		much more important. Why? because you need to iterate so much. I never said that you should
		overengineered and made your product really overkill, or in the other hand even underkill in the
		MVP stages (or any stage in that matter), all you need is that sweet spot.
	</p>
	<br />
	<p>
		If <a
			href="https://www.levels.fyi/blog/scaling-to-millions-with-google-sheets.html"
			target="_blank">using Google Sheet as your backend to serve millions of users</a
		> is your sweet spot, go for it.
	</p>
	<h3 class="head-section">Great User Experience</h3>
	<p>
		This part is what often boils me so much. A lot of people sacrificing the user experience so
		much in the name of: <i>Hey, my product is just in an MVP stage</i>.
	</p>
	<br />
	<p>
		No matter what stages your product is, if the user's experience of using your product itself is a
		hell, then your product is bound to fail.
	</p>
	<br />
	<p>
		Great user's experience here is not that your product must be very well designed, there are teams of UX
		researcher spending the majority of your budget for this MVP, no. In fact it's is the opposite.
	</p>
	<br />
	<p>
		You as the product builder (the problem solver) need to always make sure that your product is
		always aligned with user's need. Your product need to exactly hitting the painpoint of the user. <b
			>AND</b
		>, your user can easily identify the value of you and your product.
	</p>
	<h3 class="head-section">Conclusion?</h3>
	<p>
		In the end, every choice is up to you as the creator. What I can say is that you need to always
		put the user's need first. Thank you. :)
	</p>

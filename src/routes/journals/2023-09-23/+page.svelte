	<h3 class="head-section">The realization</h3>
	<p>
		Recently, I had an amazing discussion with one of my coworkers (kudos to <a
			href="https://www.linkedin.com/in/asri-hanif-ronaza/"
			target="_blank">him</a
		>). It starts as a casual, random discussion. But, along the way, we started
		to talk more deeply about product and team management.
	</p>
	<br />
	<p>
		He throws a question to me, in general like: <i
			>"Hisam, how's your opinion about the team/people allocation in our
			company right now?"</i
		>. I just replied straight-forwardly like: <i>"I think it's okay. Why?"</i>,
		I asked curiously.
	</p>
	<br />
	<p>
		He replies: <i
			>"It&apos;s good. But in my previous company, we took a different approach
			to how we allocate team members"</i
		>. <i>"Really, how?"</i> I asked again. If you can see my eyes at that moment,
		you&apos;ll see the eye of a person who&apos;s curious, confused, and all other
		curious-like emotions combined.
	</p>
	<br />
	<h3 class="head-section">The options</h3>
	<p>
		<i
			>"While here, [especially the engineering team] we allocate people to a
			project based off of
			<b>the availability</b>, the other counter option is we allocate people
			based off of
			<b>the assignment to product/feature itself</b>"</i
		> he explains.
	</p>
	<br />
	<p>
		If I am being honest with you, my dear reader, that moment has stuck in my
		mind for quite a while now. Why?
	</p>
	<br />
	<p>Let me take you to dig a little deeper into both options first.</p>
	<ul class="ml-6 list-disc !mt-1">
		<li>
			Allocation based on availability: This is the approach that most of the
			startups/small companies take. Everyone in the team has no dedicated
			product/feature that they own. Everyone owns every product. As long as
			they&apos;re available to work on it, they can take it.
		</li>
		<li>
			Allocation based on assignment: On the contrary, this approach was taken
			by most of the established companies. Everyone has their own dedicated
			product they owned that has been assigned in the beginning.
		</li>
	</ul>
	<br />
	<p>Which one to choose/the best option? <u>It depends</u>.</p>
	<br />
	<p>
		To find out, we might need to ask why big and small teams/companies took
		different approaches.
	</p>
	<h3 class="head-section">The Bargain</h3>
	<p>
		It all boils down to one word: RESOURCES. It includes time, manpower,
		capital, and many others.
	</p>
	<br />
	<p>
		The allocation based on the assignment approach promises that a product will
		always have some team/people that have a great depth of understanding of the
		product itself. From there, great development, issue resolution, and product
		ownership are highly likely guaranteed.
	</p>
	<br />
	<p>
		On the other hand, allocation based on availability promises that everyone
		(at least) knows the inner work of the product. This approach is an oasis
		for small companies/startups whose small/limited amount of product line,
		manpower, and time.
	</p>
	<h3 class="head-section">The Risks</h3>
	<p>
		Up to this point, you might have figured out the disadvantages of the
		approach itself. The assignment approach poses a huge risk when a huge task
		when an important team member leaves the company. The knowledge transfer
		MUST be smooth.
	</p>
	<br />
	<p>
		The availability approach is not the best either. Since everyone has a
		"not-so-deep" understanding of the inner work (unless all of your team is
		A++ people in terms of skills), development and issue resolution can become
		a challenge if the communication between team members is not done right.
	</p>
	<br />
	<p>So my dear reader, which one did you choose?</p>

<div class="content">
	<h3 class="head-section">The realization</h3>
	<p>
		Recently, I had an amazing discussion with one of my coworker (kudos to <a
			href="https://www.linkedin.com/in/asri-hanif-ronaza/"
			target="_blank">him</a
		>). It starts as a casual, random discussion. But, along the way, we start to talk deeper about
		product and team management.
	</p>
	<br />
	<p>
		He throws a question to me, in general was like: <i
			>"Hisam, how's you opinion about the team/people allocation in our company right now?"</i
		>. I just reply straight-forwaredly like: <i>"I think it&apos;s okay. Why?"</i> I asked curiously.
	</p>
	<br />
	<p>
		He replies: <i
			>"It&apos;s good. But in my previous company, we took a different approach on how we allocate
			team members"</i
		>. <i>"Really, how?"</i> I asked again. If you can see my eyes at that moment, you&apos;ll see an
		eye of a person who&apos;s curious, confused, and all other curious-like emotions combined.
	</p>
	<br />
	<h3 class="head-section">The options</h3>
	<p>
		<i
			>"While here, [especially the engineering team] we allocate people to a project based off of
			<b>the availability</b>, the other counter option is we allocate people based off of
			<b>the assignment to product/feature itself</b>"</i
		> he explains.
	</p>
	<br />
	<p>
		If I am being honest with you, my dear reader, that moment has stucked in my mind quite a while
		now. Why?
	</p>
	<br />
	<p>Let me take you to dig a little deep on both options first.</p>
	<ul class="ml-6 list-disc !mt-1">
		<li>
			Allocation based on availability: This is the approach that most of the startup/small
			companies take. Everyone in the team has no dedicated product/feature that they own. Everyone
			own every product. As long as they&apos;re available to work on it, they can take it.
		</li>
		<li>
			Allocation base on assignment: On the contrary, this approach was taken by most of the
			established companies. Everyone has their own dedicated product they owned that has been
			assigned in the beginning.
		</li>
	</ul>
	<br />
	<p>Which one to choose/the best option? <u>It depends</u>.</p>
	<br />
	<p>To find out, we might need to ask why big and small team/companies took different approach?</p>
	<h3 class="head-section">The Bargain</h3>
	<p>
		It all boils down to one word: RESOURCES. It includes time, manpower, capital, and many others.
	</p>
	<br />
	<p>
		The allocation based on assignment approach promises that a product will always have some
		team/people that has a great depth of understanding of the product itself. From there, a great
		development, issue resolvment, and product ownership is highly likely guaranteed.
	</p>
	<br />
	<p>
		On the other hand, allocation based on availability promises that everyone (at least) know the
		inner-work of the product. This approach is an oasis for small companies/startup who's
		small/limited amount of product-line, manpower, and time.
	</p>
	<h3 class="head-section">The Risks</h3>
	<p>
		Up to this point, you might have figured out the disanvatages of those approach itself. The
		assignment approach poses a huge risk when a huge tasks when an important team member left the
		company. The knowledge transfer MUST be smooth.
	</p>
	<br />
	<p>
		The availability approach is not the best either. Since everyone has "not-so-deep" understanding
		of the innerwork (unless all of your team-member is A++ people in term of skills), development
		and issue resolvment can become a challenge if the communication between team members are not
		done right.
	</p>
	<br />
	<p>So my dear reader, which one you choose?</p>

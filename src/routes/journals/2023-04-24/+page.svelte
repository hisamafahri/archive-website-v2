<div class="content">
	<p>
		I've always been fascinated by kingdomness, its historical importance, and
		of course the drama of its succession and the transfer of power. What makes
		me so fascinated is the fact that I can learn a lot from its story then I
		can apply it to my own self.
	</p>
	<br />
	<p>
		Recently, I've rewatched <a
			href="https://youtu.be/jNgP6d9HraI?t=321"
			target="_blank">CGP Grey's video</a
		> on the history of the British Royal Family. But one line of that video always
		stuck in my mind ever since I watched that video for the first time:
	</p>
	<br />
	<h2 class="text-center font-semibold text-lg italic">
		"Rule 0: Keep your army happy"
	</h2>
	<br />
	<p>
		That line always stuck in my mind, and I was wondering: "What does the rule
		0 for today's manager (especially PM)?"
	</p>
	<h3 class="head-section">Why Rule 0 Exist?</h3>
	<p>
		In a team (or an army) there will always be a hierarchical order whether you
		want it or not. It was just human nature to keep things organised and,
		believe it or not, to reduce the amount of conflict between each other.
	</p>
	<br />
	<p>
		But, just because you as a leader are the authoritative ones, that doesn't
		mean you can do as you please. <i
			>You become a leader because you have a team, not vice versa</i
		>. That's why, in order to maintain your position as the leader, you have to
		keep your team happy aka the Rule 0.
	</p>
	<h3 class="head-section">Why PM Should Care About This Rule?</h3>
	<p>
		In today's team (especially a software engineering team), the Product
		Managers (PMs) or something similar role have become sort of the "leader",
		or sort of the "king" in the kingdom context. That's because the PM role has
		a say in the direction of the products and their development itself.
	</p>
	<br />
	<p>
		And, as I've stated in the earlier section, the PM (as the leader) should
		care a lot about this rule in order to maintain their position. Even though
		their job security depends a lot on their performance and the higher-ups'
		decision, following or even abandoning Rule 0 will directly impact both of
		those aspects.
	</p>
	<br />
	<p>
		A lot of you up until now might be thinking that making our teams happy
		means that we have to give them a really low expectation for their work,
		never have a proper discussion with the team and just say "Yes" to every
		single input, never push their team. No, in fact, it's the opposite.
	</p>
	<br />
	<p>
		As an engineer, we love <b>consistency</b>. A consistent development flow, a
		consistent decision, and of course consistent management.
	</p>
	<br />
	<p>
		A great discussion, and a manageable debate, are really great starting
		points for implementing Rule 0. Because leading is hard, there are countless
		aspects of it. But, we need to start somewhere.
	</p>
	<h3 class="head-section">Conclusion</h3>
	<p>
		If in the past, the consequences of not following this rule are losing the
		throne or even death, in today's age it might not be like that but your
		and/or your team's destiny might be at stake. Then, good luck :).
	</p>

<div class="content">
	<p>Dear readers,</p>
	<br />
	<p>
		It's sure quite a while since the last time you've heard (or read to be
		precise) some journals from me. I beg your apologies for that. A lot of work
		always keeps my thoughts wandering around my mind and my physical notebooks.
	</p>
	<br />
	<p>
		But today, I'll assure you that we'll venture into a topic that's quite
		interesting one.
	</p>
	<br />
	<h3 class="head-section">The Purpose</h3>
	<p>
		If I were to ask you, my dear reader, what's the purpose of your learnings?
		Is it for future careers? Or might be for your curiosity? Or even simpler,
		you just learn something you and yourself need to understand or do at the
		present?
	</p>
	<br />
	<p>
		Whatever it might be, as long as you know the purpose and you are always
		eager to learn, you're good to go.
	</p>
	<h3 class="head-section">The Box</h3>
	<p>
		Now, since you've set the foundation (aka the grandeur purpose) for your
		learning, you might be asking, what should you 'learn'?
	</p>
	<br />
	<p>
		Even though I am still considering myself pretty early in my career (and
		life itself), I've already witnessed a lot of people who want to get
		something in their life (success, wealth, yada-yada), but have no single
		idea of 'what' should they do (not even reach the 'how' they do it).
	</p>
	<br />
	<p>
		I always consider the knowledge, skills, and other supporting factors that
		directly or indirectly help us to achieve those goals are <i
			>The Toolbox for Our Dreams</i
		>.
	</p>
	<br />
	<p>
		Thus, if we are using the same analogy as stated above, which box would you
		choose?
	</p>
	<br />
	<ol>
		<li>
			a) A box with a small amount of tools. But, each tool does its primary job
			flawlessly. <b>(The specialists' box)</b>
		</li>
		<li>
			b) A box with a large amount of tools. Each of the tools does its primary
			job decently (or is even considered poorly in some aspects). <b
				>(The generalists' box)</b
			>
		</li>
	</ol>
	<h3 class="head-section">The Answer</h3>
	<p>
		Well, if you are reading up to this point and hoping to get an answer to
		those choices out of this journal, I'm very sorry my dear reader I don't
		have the answer for you.
	</p>
	<br />
	<p>
		But, hear me out. Even though I don't have the answer for you, I certainly
		do have the answer for myself: <b>I choose both</b>.
	</p>
	<br />
	<p>
		Why? Since the beginning, the analogy is never (and will never be) fit to
		the whole story of our life. As life itself is complicated, fitting our mind
		frame into one single idea (whether it's to always become a specialist or a
		generalist) is like a small broken box. Limited in size, and doesn't have a
		chance to add more tools (and probably will not look that good too).
	</p>
	<br />
	<p>
		By having multiple boxes, we can position ourselves depending on our
		circumstances. If you're in a position as a leader, it might be best for you
		to become a generalist so then you can help your subordinates to make
		decisions themselves and grow.
	</p>
	<br />
	<p>
		Or, if you're in a position as a student, you might want to have the box
		have nothing in it at all. So you can absorb new "tools" more efficiently.
	</p>

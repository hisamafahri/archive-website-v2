<div class="content">
	<h3 class="head-section">The story</h3>
	<p>
		As a <a href="https://neovim.org" target="_blank">Neovim</a> user, I have been
		"asked" why on earth I use Neovim for my daily driver for building products and
		doing some general programming. I've given a lot of various answers over time,
		starting from boasting about the performance benefits, the flex of not touching
		my mouse, and so on.
	</p>
	<br />
	<p>
		But over time, as I have grown my understanding, and widened my view of this
		world, I have found a few key reasons that I might think are why I keep
		sticking to using Neovim.
	</p>
	<br />
	<p>
		I'd like to make this journal my go-to answer if in the future some people
		are asking me why I do the way I do. Not because I don't want to answer
		them. But, I'd like to give them the most thorough, clear, and concise
		answers possible.
	</p>

	<h3 class="head-section">I "own" it</h3>
	<p>
		I am not the creator of Vim or Neovim, I am not that old (sorry, Bram✌️).
		But, since Neovim itself, after the installation, will only give you the
		bare minimum things (like minimum), I have to add some plugins, configure
		it, and manage it to be able to use it as I want to.
	</p>
	<br />
	<p>
		I think the last sentences speak a lot: <i>... as I want it to be</i>.
	</p>
	<br />
	<p>
		Building, configuring, and managing the tools, that a Software Engineer uses
		in your day-to-day life makes me always feel excited every time I open my
		terminal and spins up my Neovim. It always prompts me to ask myself:
		<i>"Ah, what should I code today?"</i>.
	</p>
	<br />
	<p>
		My setups are so personalised for me that's why it is called a "Personalised
		Development Environment".
	</p>

	<h3 class="head-section">It prompts me to learn more</h3>
	<p>
		I've never heard about <a
			href="https://en.wikipedia.org/wiki/Language_Server_Protocol"
			target="_blank">LSP</a
		> before, but I have to configure the LSP on my editor.
	</p>
	<br />
	<p>
		I've never heard about <a
			href="https://en.wikipedia.org/wiki/Abstract_syntax_tree"
			target="_blank">AST</a
		>, parser or any other cool computer science terms before. But I have to at
		least have some grasp on them to understand how my editor works.
	</p>

	<h3 class="head-section">it's fun</h3>
	<p>
		I realised that building your PDE requires time and energy. I can give you
		all of the technical advantages and try to convince you to join the
		bandwagon. but, if you ask me why I keep doing this, is because it is fun
		for me.
	</p>
	<br />
	<p>
		I can spend my night just to choose the best colour scheme that suits my
		needs and liking. <i>It was fun</i>.
	</p>
	<br />
	<p>
		I can spend some time figuring out the best key bindings for my frequent
		tasks <i>It was fun</i>.
	</p>
	<h3 class="head-section">Should you try?</h3>
	<p>
		Yes! But, in the end, I realised that the most important thing is that you
		enjoy what you do. If you think you are much more comfortable working in
		Visual Studio Code, IntelliJ, or anything else (even Notepad), go for it.
	</p>
